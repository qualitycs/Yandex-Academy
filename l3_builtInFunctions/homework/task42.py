num1 = float(input())
num2 = float(input())
operator = input()
if num2 == 0:
    print('888888')
elif operator == '+':
    print(num1 + num2)
elif operator == '-':
    print(num1 - num2)
elif operator == '*':
    print(num1 * num2)
elif operator == '/':
    print(num1 / num2)
else:
    print('888888')
